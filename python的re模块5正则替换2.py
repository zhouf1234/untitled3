import re

#re.sub()替换字符串中的匹配项
mes = """
    小米说:"1,2",
    小李说:"33",
    小也说:"可以有"
"""
print(mes)
print()

#把字符串的英文引号换成汉字引号,\1意为代表一个模式单元
msg = re.sub('"(.*)"',r'“\1”',mes)
print(msg)
print()

ms = """
    小米说:"读一下这些书：红楼梦、西游记、三国演义、水浒传。"
"""
print(ms)

#上面的书名加个《
s = re.sub('(\w+)(?=[、。])',r'《\1》',ms)  #(?=[、。])先行断言，\1等同一个模式单元
print(s)